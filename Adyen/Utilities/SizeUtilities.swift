import Foundation
import UIKit

internal func + (lhs: CGSize, rhs: CGSize) -> CGSize {
    CGSize(width: lhs.width + rhs.width, height: lhs.height + rhs.height)
}

extension UIEdgeInsets {
    
    internal var size: CGSize { CGSize(width: left + right, height: top + bottom) }
    
}
